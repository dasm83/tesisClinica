package com.tesis.clinicapp.web.form.maintenance;

public class citasMainForm {
	
	private Long idcita;
	private String description;
	private String namePacient;
	private String date;
	
	
	public Long getIdcita() {
		return idcita;
	}
	public void setIdcita(Long idcita) {
		this.idcita = idcita;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getNamePacient() {
		return namePacient;
	}
	public void setNamePacient(String namePacient) {
		this.namePacient = namePacient;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
